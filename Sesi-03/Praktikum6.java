public class Praktikum6{
	public static void main(String[] args){
		int x = 10;
		long y = x;
		System.out.println("Type casting int ke long = "+y);
		long a = 5;
		int b = (int) a;
		System.out.println("Type casting int ke long = "+b);
		byte v = 10;
		short w = v;
		System.out.println("Type casting byte ke short = "+w);
		short c = 5;
		byte d = (byte) c;
		System.out.println("Type casting short ke byte = "+d);
	}
}